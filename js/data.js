// Genre mapping for filters
export const genreMap = {
    'Lo-Fi Hip Hop': 'lofi',
    'Synthwave': 'synth',
    'Trap': 'trap',
    'Ambient': 'ambient',
    'Techno': 'techno',
    'Reggaeton': 'reggaeton',
    'Indie Electronic': 'indie',
    'Cyberpunk': 'cyber',
    'Jazz Hop': 'jazz',
    'Cinematic': 'cinematic'
};

// Get album by ID
export function getAlbumById(id) {
    return albums.find(a => a.id === id) || albums[0];
}

// Get albums by genre
export function getAlbumsByGenre(genre) {
    if (genre === 'all') return albums;
    return albums.filter(a => genreMap[a.genre] === genre);
}

// Search albums
export function searchAlbums(query) {
    const q = query.toLowerCase();
    return albums.filter(a => 
        a.title.toLowerCase().includes(q) ||
        a.artist.toLowerCase().includes(q) ||
        a.genre.toLowerCase().includes(q) ||
        a.tags.some(t => t.includes(q))
    );
}
